
public class UnaryOperatorApp {

	public static void main(String[] args) {
	int i = 0;
	// increment by 1
	i++;
	// or 
	i =i+1;
	i += 1;
//now i = 3
	System.out.println(i);

// increment by 3
	i=i+3;
	i +=3;
// now i = 9
	System.out.println(i);
	
// reduce i by 1
	i--;
	System.out.println(i);
	
	}

}
